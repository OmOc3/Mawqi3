export const exactArabicToEnglish = new Map<string, string>(exactArabicToEnglishEntries);

const dynamicArabicToEnglishRules: Array<[RegExp, (match: RegExpMatchArray) => string]> = [
  [/^صورة المحطة (.+)$/u, (match) => `Station photo ${match[1]}`],
  [/^تحديد المحطة (.+) للتصدير$/u, (match) => `Select station ${match[1]} for export`],
  [/^محطة (.+)$/u, (match) => `Station ${match[1]}`],
  [/^المحطة (.+)$/u, (match) => `Station ${match[1]}`],
  [/^مرحبًا، (.+)$/u, (match) => `Welcome, ${match[1]}`],
  [/^(.+) تقارير$/u, (match) => `${match[1]} reports`],
  [/^(.+) محطات$/u, (match) => `${match[1]} stations`],
  [/^(.+) طلبات$/u, (match) => `${match[1]} orders`],
  [/^(\d+(?:[.,]\d+)?)\s*كم$/u, (match) => `${match[1]} km`],
  [/^(\d+(?:[.,]\d+)?)\s*دقيقة$/u, (match) => `${match[1]} minutes`],
  [/^(\d+(?:[.,]\d+)?)\s*ساعة$/u, (match) => `${match[1]} hours`],
  [/^(\d+)\s*د$/u, (match) => `${match[1]}m`],
  [/^(\d+)\s*س$/u, (match) => `${match[1]}h`],
  [/^(\d+)س\s+(\d+)د$/u, (match) => `${match[1]}h ${match[2]}m`],
  [/^(\d+) س\s+(\d+) د$/u, (match) => `${match[1]}h ${match[2]}m`],
];

const whitespacePattern = /\s+/g;
const arabicPattern = /[\u0600-\u06FF]/u;

export function hasArabicText(value: string): boolean {
  return arabicPattern.test(value);
}

export function normalizeTranslatableText(value: string): string {
  return value.replace(whitespacePattern, " ").trim();
}

export function translateArabicText(value: string): string {
  const normalizedValue = normalizeTranslatableText(value);

  if (!hasArabicText(normalizedValue)) {
    return value;
  }

  const exactTranslation = exactArabicToEnglish.get(normalizedValue);

  if (exactTranslation) {
    return exactTranslation;
  }

  for (const [pattern, translate] of dynamicArabicToEnglishRules) {
    const match = normalizedValue.match(pattern);

    if (match) {
      return translate(match);
    }
  }

  return value;
}
